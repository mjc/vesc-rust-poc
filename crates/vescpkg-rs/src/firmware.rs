/// Compute the firmware `PROG_ADDR` for the package image that owns a symbol.
///
/// Package authors should pass a package-local callback symbol and receive a
/// typed `PackageProgramAddress`; the raw VESC name stays here because this is
/// the SDK boundary that mirrors `vesc_pkg_lib`.
///
/// VESC's loader identifies a native package by its loaded image base. The
/// package entry macro exports a tiny relocation-free helper next to `prog_ptr`
/// that returns that address regardless of where this callback is linked.
///
/// C map: `PROG_ADDR` is defined as `((uint32_t)&prog_ptr)` in
/// `third_party/vesc_pkg_lib/vesc_c_if.h:697`; VESC stores and calls that
/// native image base at `third_party/vesc/lispBM/lispif_c_lib.c:1087-1100`, and
/// `lib_get_arg` later matches it at `third_party/vesc/lispBM/lispif_c_lib.c:152-156`.
#[macro_export]
macro_rules! firmware_package_program_address {
    ($symbol:path) => {{
        let _ = $symbol;
        unsafe extern "C" {
            fn __vescpkg_program_address() -> u32;
        }
        // SAFETY: `package_start!` defines this package-local helper with the
        // declared C ABI and return type.
        $crate::__macro_support::PackageProgramAddress::new(unsafe { __vescpkg_program_address() })
    }};
}

/// Macro implementation hook for scoped firmware callback state access.
#[doc(hidden)]
#[cfg(not(test))]
pub unsafe fn __firmware_package_state_ptr<T: 'static>(
    program: crate::PackageProgramAddress,
) -> Option<NonNull<T>> {
    let bindings = crate::bindings::RealBindings;
    unsafe { crate::bindings::AppDataBindings::arg_state_ptr::<T>(&bindings, program) }
}

/// Copy a fixed-size firmware array into Rust-owned storage.
///
/// # Safety
///
/// `values` must be null or point to `N` initialized, readable `T` values.
pub unsafe fn firmware_array<T: Copy, const N: usize>(values: *const T) -> Option<[T; N]> {
    let values = NonNull::new(values.cast_mut())?;
    let values = unsafe { core::slice::from_raw_parts(values.as_ptr().cast_const(), N) };
    values.try_into().ok()
}

unsafe fn borrowed_slice<'a, T>(data: *const T, len: usize) -> Option<&'a [T]> {
    if len == 0 {
        return Some(&[]);
    }

    len.checked_mul(core::mem::size_of::<T>())
        .filter(|bytes| isize::try_from(*bytes).is_ok())?;

    let data = NonNull::new(data.cast_mut())?;
    Some(unsafe { core::slice::from_raw_parts(data.as_ptr(), len) })
}

unsafe fn mutable_bytes<'a>(data: *mut u8, len: usize) -> Option<MutablePacket<'a>> {
    let data = NonNull::new(data)?;
    Some(MutablePacket(unsafe {
        core::slice::from_raw_parts_mut(data.as_ptr(), len)
    }))
}

