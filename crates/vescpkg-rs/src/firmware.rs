/// Register custom-config callbacks resolved into a loaded native image.
///
/// VESC records the loaded native image base at `third_party/vesc/lispBM/lispif_c_lib.c:1095-1098`,
/// and custom-config registration checks loaded function pointers in
/// `third_party/vesc/conf_custom.c:34-42`.
///
/// # Safety
///
/// `image` must own all three callback addresses, and resolving each address
/// must produce a valid executable function pointer that remains installed
/// until the callbacks are cleared.
#[cfg(test)]
unsafe fn register_custom_config_callbacks_from_image<B>(
    bindings: &B,
    image: NativeImage,
    get_cfg: crate::ffi::CustomConfigGet,
    set_cfg: crate::ffi::CustomConfigSet,
    get_cfg_xml: crate::ffi::CustomConfigXml,
) -> bool
where
    B: crate::bindings::CustomConfigBindings,
{
    bindings.register_custom_config_callbacks(
        resolve_custom_config_get_callback(image, get_cfg),
        resolve_custom_config_set_callback(image, set_cfg),
        resolve_custom_config_xml_callback(image, get_cfg_xml),
    )
}

