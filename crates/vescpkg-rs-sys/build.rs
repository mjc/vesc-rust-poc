//! Generate VESC firmware function-table descriptors from the pinned upstream header.

use std::{
    env,
    fmt::Write as _,
    fs,
    path::{Path, PathBuf},
    process::Command,
};

#[path = "build/vesc_if.rs"]
mod vesc_if;

use vesc_if::SlotDeclaration;

fn main() {
    let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").expect("manifest dir"));
    let workspace = manifest_dir.join("../..");
    let header = workspace.join("third_party/vesc_pkg_lib/vesc_c_if.h");
    let out = PathBuf::from(env::var("OUT_DIR").expect("out dir")).join("c_vesc_if.rs");

    println!("cargo::rustc-check-cfg=cfg(coverage)");
    println!("cargo:rerun-if-changed={}", header.display());
    println!(
        "cargo:rerun-if-changed={}",
        workspace.join("third_party/vesc_pkg_lib/.git").display()
    );

    let source = fs::read_to_string(&header).expect("third_party/vesc_pkg_lib/vesc_c_if.h");
    let slots = vesc_if::parse(&source)
        .unwrap_or_else(|error| panic!("failed to parse {}: {error}", header.display()));
    assert!(
        !slots.is_empty(),
        "failed to parse vesc_c_if slots from {}",
        header.display()
    );

    fs::write(out, generated_rust(&workspace, &slots)).expect("write generated c_vesc_if.rs");
}

fn generated_rust(workspace: &Path, slots: &[SlotDeclaration]) -> String {
    let commit = submodule_commit(workspace).unwrap_or_else(|| "unknown".to_owned());
    let mut rust = String::new();

    rust.push_str("// Generated by crates/vescpkg-rs-sys/build.rs from ");
    rust.push_str("third_party/vesc_pkg_lib/vesc_c_if.h.\n");
    rust.push_str("// Do not edit by hand; update the vesc_pkg_lib submodule instead.\n\n");
    rust.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq)]\n");
    rust.push_str("pub(crate) struct Slot {\n");
    rust.push_str("    pub(crate) name: &'static str,\n");
    rust.push_str("    pub(crate) index: usize,\n");
    rust.push_str("    pub(crate) vesc32_byte_offset: usize,\n");
    rust.push_str("    pub(crate) header_line: usize,\n");
    rust.push_str("}\n\n");
    rust.push_str(
        "pub(crate) const HEADER_REPO: &str = \"https://github.com/lukash/vesc_pkg_lib\";\n",
    );
    writeln!(rust, "pub(crate) const HEADER_COMMIT: &str = \"{commit}\";")
        .expect("write generated Rust");
    writeln!(
        rust,
        "pub(crate) const FIELD_COUNT: usize = {};",
        slots.len()
    )
    .expect("write generated Rust");
    rust.push('\n');
    rust.push_str("pub(crate) const SLOTS: [Slot; FIELD_COUNT] = [\n");
    for slot in slots {
        writeln!(
            rust,
            "    Slot {{ name: \"{}\", index: {}, vesc32_byte_offset: {}, header_line: {} }},",
            slot.c_name,
            slot.index,
            slot.index * 4,
            slot.line
        )
        .expect("write generated Rust");
    }
    rust.push_str("];\n\n");

    for slot in slots {
        writeln!(rust, "pub(crate) mod {} {{", slot.rust_name).expect("write generated Rust");
        writeln!(
            rust,
            "    pub(crate) const NAME: &str = \"{}\";",
            slot.c_name
        )
        .expect("write generated Rust");
        writeln!(rust, "    pub(crate) const INDEX: usize = {};", slot.index)
            .expect("write generated Rust");
        rust.push_str("    pub(crate) const VESC32_BYTE_OFFSET: usize = INDEX * 4;\n");
        writeln!(
            rust,
            "    pub(crate) const HEADER_LINE: usize = {};",
            slot.line
        )
        .expect("write generated Rust");
        rust.push_str("}\n\n");
    }

    rust
}

fn submodule_commit(workspace: &Path) -> Option<String> {
    let output = Command::new("git")
        .args(["-C", "third_party/vesc_pkg_lib", "rev-parse", "HEAD"])
        .current_dir(workspace)
        .output()
        .ok()?;
    output
        .status
        .success()
        .then(|| String::from_utf8_lossy(&output.stdout).trim().to_owned())
        .filter(|commit| !commit.is_empty())
}
